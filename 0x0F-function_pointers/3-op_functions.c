#include <stdlib.h>
#include <stdio.h>
#include "3-calc.h"
/**
  * op_add - add
  *
  * @a: add
  * @b: add
  *
  * Return: res
  */
int op_add(int a, int b)
{
return (a + b);
}

/**
  * op_sub - sub
  *
  * @a: sub
  * @b: sub
  *
  * Return: res
  */
int op_sub(int a, int b)
{
return (a - b);
}
/**
  * op_mul - mul
  *
  * @a: mul
  * @b: mul
  *
  * Return: res
  */
int op_mul(int a, int b)
{
return (a * b);
}

/**
  * op_div - div
  *
  * @a: div
  * @b: div
  *
  * Return: div
  */
int op_div(int a, int b)
{
return (a / b);
}
/**
  * op_mod - mod
  *
  * @a: mod
  * @b: mod
  *
  * Return: res
  */
int op_mod(int a, int b)
{
return (a % b);
}
